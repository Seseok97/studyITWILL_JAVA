class NoThread{
	String name;
	int count;
	
	public NoThread(String name, int count) {
		super();
		this.name = name;
		this.count = count;
	}// const end
	
	public void run() {
		for(int i=1 ; i<=count ; i++) {
			System.out.println(name+" : "+i);
		}// for end
	}// run() method end
	
	
}// NoThread class end



































